//Realização de operações simples
	
public class Testes_Operadores {

	public static void main(String[] args) {

		// a) 2*6/3
		double a = 2 * 6 / 3;
		System.out.println("Letra a): " + a);

		// b) 3 + 2 * 4
		double b = 3 + 2 * 4;
		System.out.println("Letra b): " + b);

		// c) (3+2)*4
		double c = (3 + 2) * 4;
		System.out.println("Letra c): " + c);

		// d) 60 / (3+2) * 4
		double d = 60 / (3 + 2) * 4;
		System.out.println("Letra d): " + d);

		// e) 60 / ((3+2) * 4)
		double e = 60 / ((3 + 2) * 4);
		System.out.println("Letra e): " + e);
	}

}
